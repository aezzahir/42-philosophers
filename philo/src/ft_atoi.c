#include "../includes/philo.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}

size_t	ft_atoi(const char *str)
{
	int			digit;
	int			sign;
	long int	result;
	int			i;

	sign = 1;
	result = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
		if (str[i++] == '-')
			sign = -1;
	while (ft_isdigit(str[i]))
	{
		digit = str[i] - '0';
		result = result * 10 + digit;
		i++;
	}
	if (str[i] != '\0')
		return (-1);
	result = sign * result;
	return ((size_t)(result));
}
